namespace DevInSales.Services
{
    public class CalculatorService
    {
        public static int Soma(int x, int y)
        {
            return  x + y;
        }

        public static int Subtrair(int x, int y)
        {
            return x - y;
        }

        public static int Multiplicar(int x, int y)
        {
            return x * y;
        }


    }
}
